import java.util.*;
public class Switch_statement {
    public static void  main(String[] args) {
        
        int number = 17;

        switch (number) {
            case 1 : System.out.println("Dhosa");
                break;
            case 2 : System.out.println("Dabeli");
                break;
            case 3 : System.out.println("Pakoda");
                break;
            default : System.out.println("Ghihoda nu shak");
                break;
        }
    }
}

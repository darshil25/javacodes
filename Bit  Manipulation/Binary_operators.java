/**
 * Binary_operators
 */
public class Binary_operators {

    public static void main(String[] args) {
        // AND(&) Operator
        System.out.println(5 & 6);

        // OR(|) Operator
        System.out.println(5 | 7);

        // XOR (^) O perator
        System.out.println(5 ^ 6);

        // 1's Complement (~)
        System.out.println(~5);

        // Binary Left Shift(<<)
        // equation => a<<b = a * (2)^b
        System.out.println(5 << 2);

        // Binary Right Shift (>>)
        // equation => a>>b = a / (2)^b
        System.out.println(6 >> 1);
    }
}